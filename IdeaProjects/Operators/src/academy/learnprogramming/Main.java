package academy.learnprogramming;

public class Main {

    public static void main(String[] args) {
        // operators: + = - * /
        int result = 3; // 1 + 2 = 3 (comments are ignored n=by the computer)
        result++; //3+1 = 4
        System.out.println("3+1 = "+ result);

        result--; //3-1 = 3
        System.out.println("3-1 = "+ result);
    }
}
